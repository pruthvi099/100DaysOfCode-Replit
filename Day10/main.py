adding = 4 + 3          # Adds 4 and 3 → result: 7
print(adding)

subtraction = 8 - 9     # Subtracts 9 from 8 → result: -1
print(subtraction)

multiplication = 4 * 32 # Multiplies 4 by 32 → result: 128
print(multiplication)

division = 50 / 5       # Divides 50 by 5 → result: 10.0 (float)
print(division)

squared = 5**2          # 5 to the power of 2 → result: 25
print(squared)

modulo = 15 % 4         # Remainder of 15 ÷ 4 → result: 3
print(modulo)

divisor = 15 // 2       # Whole number division (floor division) → result: 7
print(divisor)
